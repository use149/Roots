              // Step 1: Create a function to populate the area options
        function populateAreaOptions() {
            // Get the select element
            const areaSelect = document.getElementById('areaSelect');
            
            // Create a set of unique areas
            let areas = new Set();
            for (const officer in officersData) {
                officersData[officer].areas.forEach(area => areas.add(area));
            }

            // Populate the area select dropdown with options
            areas.forEach(area => {
                const option = document.createElement('option');
                option.value = area;
                option.textContent = area;
                areaSelect.appendChild(option);
            });
        }

        // Step 2: Function to fetch officer data based on the selected area
        function fetchOfficerData() {
            const selectedArea = document.getElementById('areaSelect').value;
            const officerDetailsDiv = document.getElementById('officerDetails');

            // Clear previous officer details
            officerDetailsDiv.innerHTML = "";

            if (selectedArea) {
                // Iterate over officers and check if they cover the selected area
                for (const officer in officersData) {
                    const officerInfo = officersData[officer];
                    if (officerInfo.areas.includes(selectedArea)) {
                        // Create a div for each officer's details
                        const officerDiv = document.createElement('div');
                        officerDiv.innerHTML = `
                            <h3>${officer}</h3>
                            <p><strong>Phone:</strong> ${officerInfo.phone}</p>
                            <p><strong>Covered Areas:</strong> ${officerInfo.areas.join(', ')}</p>
                        `;
                        officerDetailsDiv.appendChild(officerDiv);
                    }
                }
            } else {
                officerDetailsDiv.innerHTML = "<p>Please select an area to see officer details.</p>";
            }
        }

